const MERGED = [...SIX, ...SEVEN];

function uniqOfLength(len: number): string[] {
  const set = new Set<string>();
  for (const w of MERGED) {
    if (w.length === len) set.add(w);
  }
  return [...set];
}

export const BLOCKED_FILL_SIX_LETTER = uniqOfLength(6);
export const BLOCKED_FILL_SEVEN_LETTER = uniqOfLength(7);
